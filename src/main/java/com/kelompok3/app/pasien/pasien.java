    public void showTablePasien() {
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Id");
        tb.addColumn("Nama");
        tb.addColumn("Usia");       
        tb.addColumn("Jenis Kelamin");        
        tb.addColumn("No Telp"); 
        tb.addColumn("Alamat");                
        try {   
            String query = "SELECT id_pasien, nama_pasien, usia, jenis_kelamin, no_telp, alamat FROM pasien";
            java.sql.Connection conn=(Connection)DBconnection.configDB();
            java.sql.Statement s = conn.createStatement();
            java.sql.ResultSet r = s.executeQuery(query);
            while (r.next()){
                tb.addRow(new Object[]{
                    r.getString(1),r.getString(2),r.getString(3),r.getString(4),r.getString(5),r.getString(6)
                });
            }
            tbPasien.setModel(tb);
            setkolom();
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    public void showTableKamar() {
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("No Kamar");
        tb.addColumn("Status");               
        try {   
            String query = "SELECT kode_kamar, status FROM kamar WHERE status = 'Kosong'";
            java.sql.Connection conn=(Connection)DBconnection.configDB();
            java.sql.Statement s = conn.createStatement();
            java.sql.ResultSet r = s.executeQuery(query);
            while (r.next()){
                tb.addRow(new Object[]{
                    r.getString(1),r.getString(2)
                });
            }
            tbKamar.setModel(tb);
            setkolom();
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

