    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        int id_pasien_kmr = 0;
        try {
            String sql = "INSERT INTO pasien (nama_pasien,usia,jenis_kelamin,alamat,no_telp) VALUES ('"+txtNama.getText()+"','"+txtUsia.getText()+"','"+cboJK.getSelectedItem()+"','"+txtaAlamat.getText()+"','"+txtNoTelp.getText()+"')";
            java.sql.Connection conn=(Connection)DBconnection.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            try {
                String sql2 = "SELECT id_pasien FROM pasien ORDER BY id_pasien DESC LIMIT 1;";
                java.sql.Connection conn2=(Connection)DBconnection.configDB();
                Statement st = conn2.createStatement();
                ResultSet rs = st.executeQuery(sql2);
                while (rs.next()) {
                    id_pasien_kmr = rs.getInt("id_pasien");
                 }
                try{
                    String sql3 = "UPDATE kamar SET id_pasien = '"+id_pasien_kmr+"', status = 'Penuh' WHERE kode_kamar = '"+txtKamar.getText()+"'";
                    Connection conn3 = (Connection)DBconnection.configDB();
                    java.sql.PreparedStatement pst3 = conn3.prepareStatement(sql3);
                    pst3.execute();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");
                    showTablePasien();
                    showTableKamar();
                    clean();
                } catch (Exception e){
                    JOptionPane.showMessageDialog(null, "Tambah Data Gagal");
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Tambah Data Gagal");
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tambah Data Gagal");
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

